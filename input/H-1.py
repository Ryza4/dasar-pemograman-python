
# deklarasi 
name = "reza"

# Cara pertama: mencetak teks dengan variabel
print(name, "hello")  # Output: reza hello

# Cara kedua: menggunakan format f-string (lebih direkomendasikan)
print(f"{name} hello")  # Output: reza hello

# Cara ketiga: menggabungkan string dengan operator +
print(name + " hello")  # Output: reza hello

